from colorama import Fore, Back, Style
# Fore - задає колір тексту в консолі
print(Fore.RED + "text")
print("some text")
# Back - задає задній кольір
print(Back.GREEN + "Green")
print("some text")
print(Fore.BLUE + "BLUE text")
# Style.DIM
print(Style.DIM +"Dim text")
# Style.RESET_ALL - задає всі налаштування
print(Style.RESENT_ALL)
print("some text")
